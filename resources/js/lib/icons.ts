/**
 * Get icon by class name
 */
export function getIconClass(name: string): string {
  return REMIXICON_LIBRARY.includes(name) ? name : "ri-question-line";
}

/**
 * Filter icons by search term
 */
export function searchIcons(query: string): string[] {
  if (!query) return REMIXICON_LIBRARY;
  const q = query.toLowerCase();
  return REMIXICON_LIBRARY.filter(icon =>
    icon.toLowerCase().includes(q)
  );
}

/**
 * Get icon name without prefix
 */
export function getIconName(iconClass: string): string {
  return iconClass.replace("ri-", "").replace("-line", "").replace("-fill", "");
}
